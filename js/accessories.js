// ============================================================
//  配件渲染引擎
// ============================================================

/**
 * 将像素网格渲染为 Canvas
 * @param {object} acc - 配件定义
 * @param {number} pixelScale - 每像素格缩放倍数
 * @param {boolean} drawOutline - 是否绘制轮廓
 * @returns {HTMLCanvasElement}
 */
function renderAccessoryToCanvas(acc, pixelScale = 3, drawOutline = true) {
  const rows = acc.pixels.length;
  const cols = Math.max(...acc.pixels.map(r => r.length));
  const outlinePad = drawOutline ? 2 : 0;
  const w = cols * pixelScale + outlinePad * 2;
  const h = rows * pixelScale + outlinePad * 2;

  const canvas = document.createElement('canvas');
  canvas.width = w;
  canvas.height = h;
  const ctx = canvas.getContext('2d');
  ctx.imageSmoothingEnabled = false;

  // 颜色映射: '#'→colors[0], '@'→colors[1], '$'→colors[2], '%'→colors[3]
  const colorMap = {
    '#': acc.colors[0] || '#000',
    '@': acc.colors[1] || acc.colors[0] || '#000',
    '$': acc.colors[2] || acc.colors[0] || '#000',
    '%': acc.colors[3] || acc.colors[0] || '#000',
  };

  const ox = outlinePad, oy = outlinePad;

  // 填充像素
  for (let row = 0; row < rows; row++) {
    const line = acc.pixels[row];
    for (let col = 0; col < line.length; col++) {
      const ch = line[col];
      if (ch === '.') continue;
      ctx.fillStyle = colorMap[ch] || acc.colors[0];
      ctx.fillRect(ox + col * pixelScale, oy + row * pixelScale, pixelScale, pixelScale);
    }
  }

  // 轮廓描边
  if (drawOutline) {
    ctx.strokeStyle = '#000000';
    ctx.lineWidth = pixelScale;
    for (let row = 0; row < rows; row++) {
      const line = acc.pixels[row];
      for (let col = 0; col < line.length; col++) {
        if (line[col] === '.') continue;
        const neighbors = [[row-1,col],[row+1,col],[row,col-1],[row,col+1]];
        let isBorder = false;
        for (const [nr,nc] of neighbors) {
          if (nr < 0 || nr >= rows || nc < 0 || nc >= acc.pixels[nr].length || acc.pixels[nr][nc] === '.') {
            isBorder = true; break;
          }
        }
        if (isBorder) {
          ctx.strokeRect(ox + col * pixelScale, oy + row * pixelScale, pixelScale, pixelScale);
        }
      }
    }
  }

  canvas._accWidth = cols;
  canvas._accHeight = rows;
  canvas._pixelScale = pixelScale;
  return canvas;
}

// ============================================================
//  配件管理器（支持 0.1x ~ 100x 缩放）
// ============================================================

/** 基础像素大小：配件定义中每 1 像素 = 5 显示像素 */
const BASE_PIXEL = 5;
const MIN_PIXEL = 1;
const MAX_PIXEL = 100;

const AccessoryManager = {
  items: [],

  /**
   * 添加配件
   * @param {object} acc - 配件定义
   * @param {number} charWidth - 角色显示宽度
   * @param {number} charHeight - 角色显示高度
   * @returns {number} 新配件索引
   */
  add(acc, charWidth, charHeight) {
    const canvas = renderAccessoryToCanvas(acc, BASE_PIXEL, true);
    const x = Math.round(charWidth / 2 - canvas.width / 2);
    const y = Math.round(charHeight * 0.05);

    const item = {
      acc: acc,
      x: x, y: y,
      scale: 1.0,             // 用户缩放倍率 (0.1 ~ 100)
      canvas: canvas,         // 基础渲染（BASE_PIXEL）
      scaledCanvas: null,     // 缩放后的缓存
    };
    item.scaledCanvas = _renderScaled(item);

    this.items.push(item);
    return this.items.length - 1;
  },

  remove(index) {
    if (index >= 0 && index < this.items.length) this.items.splice(index, 1);
  },

  clear() { this.items = []; },

  moveTo(index, x, y) {
    if (index >= 0 && index < this.items.length) {
      this.items[index].x = Math.round(x);
      this.items[index].y = Math.round(y);
    }
  },

  /** 设置缩放并重新渲染 */
  setScale(index, newScale) {
    if (index >= 0 && index < this.items.length) {
      const item = this.items[index];
      item.scale = Math.max(0.1, Math.min(10, Math.round(newScale * 10) / 10));
      item.scaledCanvas = _renderScaled(item);
    }
  },

  /** 获取缩放后的画布 */
  getScaledCanvas(index) {
    if (index >= 0 && index < this.items.length) {
      return this.items[index].scaledCanvas || this.items[index].canvas;
    }
    return null;
  },

  /** 合成最终图片 */
  composite(charCanvas) {
    const result = document.createElement('canvas');
    result.width = charCanvas.width;
    result.height = charCanvas.height;
    const ctx = result.getContext('2d');
    ctx.imageSmoothingEnabled = false;
    ctx.drawImage(charCanvas, 0, 0);

    for (const item of this.items) {
      const c = item.scaledCanvas || item.canvas;
      ctx.drawImage(c, item.x, item.y);
    }
    return result;
  },

  /** 命中测试 */
  hitTest(mx, my) {
    for (let i = this.items.length - 1; i >= 0; i--) {
      const item = this.items[i];
      const c = item.scaledCanvas || item.canvas;
      if (mx >= item.x && mx <= item.x + c.width &&
          my >= item.y && my <= item.y + c.height) return i;
    }
    return -1;
  },
};

/** 内部：根据用户缩放倍率重新渲染配件 */
function _renderScaled(item) {
  const rawScale = Math.round(BASE_PIXEL * item.scale);
  const effectiveScale = Math.max(MIN_PIXEL, Math.min(MAX_PIXEL, rawScale));
  return renderAccessoryToCanvas(item.acc, effectiveScale, true);
}


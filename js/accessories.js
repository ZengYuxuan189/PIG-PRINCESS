function getAccessoriesByCategory(catId) {
  if (catId === 'all') return [...ACCESSORIES];
  return ACCESSORIES.filter(a => a.cat === catId);
}

function findAccessory(id) {
  return ACCESSORIES.find(a => a.id === id);
}


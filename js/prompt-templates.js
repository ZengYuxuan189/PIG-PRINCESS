/**
 * 根据 ID 查找模板
 * @param {string} id - 模板 ID
 * @returns {object|undefined} 模板对象
 */
function findTemplate(id) {
  return PROMPT_TEMPLATES.find(t => t.id === id);
}

/**
 * 获取默认模板（经典NES角色）
 * @returns {object}
 */
function getDefaultTemplate() {
  return PROMPT_TEMPLATES[0];
}

/**
 * 计算提示词的实际字数（去除空白字符后的中文字符数）
 * @param {string} text
 * @returns {number}
 */
function countPromptChars(text) {
  if (!text) return 0;
  // 去除空白字符（空格、换行、制表符）后计算长度
  return text.replace(/[\s\n\r\t]/g, '').length;
}

/**
 * 检查提示词是否超过上限
 * @param {string} text
 * @returns {boolean}
 */
function isPromptTooLong(text) {
  return countPromptChars(text) > 300;
}

// 显式挂载到 window（const 在全局作用域不会自动成为 window 属性）
window.PROMPT_TEMPLATES = PROMPT_TEMPLATES;
window.findTemplate = findTemplate;
window.getDefaultTemplate = getDefaultTemplate;
window.countPromptChars = countPromptChars;
window.isPromptTooLong = isPromptTooLong;
